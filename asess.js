let score = 100;

if (score > 100 || score < 0){
  console.log("Error: invalid score!")
}
if (score >= 90) {
  console.log("You got an A");

} else if (score >= 80) {
  console.log("You got a B");

} else if (score >= 70) {
  console.log("You got a C");

} else if (score >= 60) {
  console.log("You got a D");

} else {
  console.log("You got an F");
}

